import math
# 1. Square root
print("square root ",math.sqrt(16))
# 2. Power
print("power ",math.pow(2, 4))
# 3. Ceil (round up)
print("ceil ",math.ceil(4.2))
# 4. Floor (round down)
print("Floor ",math.floor(4.8))
# 5. Absolute value
print("Absolute : ",math.fabs(-25))
# 6. Factorial
print("Factorial ",math.factorial(5))
# 7. PI constant
print("PI : ",math.pi)
# 8. Sin value
print("SIN :",math.sin(math.radians(90)))
# 9. Log base 10
print("LOG BASE 10",math.log10(1000))
# 10. GCD
print("GCD",math.gcd(20, 28))
